// Entity class
class Music{
  //----- Parameters
  String title;
  String artist;
  String imagePath;
  String urlSong;

  //-----Constructor
  Music(  String title, String artist,  String imagePath, String urlSong){
    this.title=title;
    this.artist=artist;
    this.imagePath=imagePath;
    this.urlSong=urlSong;
  }

}